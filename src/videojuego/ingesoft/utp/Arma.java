package videojuego.ingesoft.utp;

public class Arma {

	public Integer codalarma;
	public Integer agilidad;
	public Integer fuerza;
	public Integer inteligencia;
	public Integer durabilidad;
	public boolean ocupada;

	public Integer getCodalarma() {
		return codalarma;
	}

	public void setCodalarma(Integer codalarma) {
		this.codalarma = codalarma;
	}

	public Integer getAgilidad() {
		return agilidad;
	}

	public void setAgilidad(Integer agilidad) {
		this.agilidad = agilidad;
	}

	public Integer getFuerza() {
		return fuerza;
	}

	public void setFuerza(Integer fuerza) {
		this.fuerza = fuerza;
	}

	public Integer getInteligencia() {
		return inteligencia;
	}

	public void setInteligencia(Integer inteligencia) {
		this.inteligencia = inteligencia;
	}

	public Integer getDurabilidad() {
		return durabilidad;
	}

	public void setDurabilidad(Integer durabilidad) {
		this.durabilidad = durabilidad;
	}

	public boolean isOcupada() {
		return ocupada;
	}

	public void setOcupada(boolean ocupada) {
		this.ocupada = ocupada;
	}

}
